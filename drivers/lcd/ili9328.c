#include "drivers/lcd/ili93xx_controllers.h"

#define ILI9328_DRIVER_OUTPUT 0x01
#define ILI9328_LCD_DRIVING_CTRL 0x02
#define ILI9328_ENTRY_MODE 0x03
#define ILI9328_RESIZE_CTRL 0x04
#define ILI9328_DISPLAY_CTRL1 0x07
#define ILI9328_DISPLAY_CTRL2 0x08
#define ILI9328_DISPLAY_CTRL3 0x09
#define ILI9328_DISPLAY_CTRL4 0x0a
#define ILI9328_RGB_DISPLAY_IFC_CTRL1 0x0c
#define ILI9328_FMARK_POS 0x0d
#define ILI9328_RGB_DISPLAY_IFC_CTRL2 0x0f
#define ILI9328_POWER_CTRL1 0x10
#define ILI9328_POWER_CTRL2 0x11
#define ILI9328_POWER_CTRL3 0x12
#define ILI9328_POWER_CTRL4 0x13
#define ILI9328_POWER_CTRL7 0x29
#define ILI9328_HADDR_SET 0x20
#define ILI9328_VADDR_SET 0x21
#define ILI9328_FRAME_RATE_AND_COLOR_CTRL 0x2b
#define ILI9328_GAMMA_CTRL1 0x30
#define ILI9328_GAMMA_CTRL2 0x31
#define ILI9328_GAMMA_CTRL3 0x32
#define ILI9328_GAMMA_CTRL4 0x35
#define ILI9328_GAMMA_CTRL5 0x36
#define ILI9328_GAMMA_CTRL6 0x37
#define ILI9328_GAMMA_CTRL7 0x38
#define ILI9328_GAMMA_CTRL8 0x39
#define ILI9328_GAMMA_CTRL9 0x3c
#define ILI9328_GAMMA_CTRL10 0x3d
#define ILI9328_HSTART 0x50
#define ILI9328_HEND 0x51
#define ILI9328_VSTART 0x52
#define ILI9328_VEND 0x53
#define ILI9328_GATE_SCAN_CTRL1 0x60
#define ILI9328_GATE_SCAN_CTRL2 0x61
#define ILI9328_GATE_SCAN_CTRL3 0x6a
#define ILI9328_PARTIAL_IMAGE1_DISPLAY_POS 0x80
#define ILI9328_PARTIAL_IMAGE1_RAM_START 0x81
#define ILI9328_PARTIAL_IMAGE1_RAM_END 0x82
#define ILI9328_PARTIAL_IMAGE2_DISPLAY_POS 0x83
#define ILI9328_PARTIAL_IMAGE2_RAM_START 0x84
#define ILI9328_PARTIAL_IMAGE2_RAM_END 0x85
#define ILI9328_PANEL_IFC_CTRL1 0x90
#define ILI9328_PANEL_IFC_CTRL2 0x92
#define ILI9328_PANEL_IFC_CTRL4 0x95
#define ILI9328_PANEL_IFC_CTRL5 0x97

const ili93xx_cmd_t ili9328_init_cmds[] = {
    /* Internal Timing */
    REG_SET(0xE3, 0x3008),
    REG_SET(0xe7, 0x0012),
    REG_SET(0xEF, 0x1231),

    REG_SET(ILI9328_DRIVER_OUTPUT, 0x0100), /* Set SS bit */
    REG_SET(ILI9328_LCD_DRIVING_CTRL, 0x0700), /* 1 line inversion */
    REG_SET(ILI9328_ENTRY_MODE, 0x0030), /* RGB mode, I/D 11 */
    REG_SET(ILI9328_RESIZE_CTRL, 0x0000),
    REG_SET(ILI9328_DISPLAY_CTRL2, 0x0202), /* 2 lines for back & front porch */
    REG_SET(ILI9328_DISPLAY_CTRL3, 0x0000), /* Non display area refresh cycle */
    REG_SET(ILI9328_DISPLAY_CTRL4, 0x0000), /* No FMARK */
    REG_SET(ILI9328_RGB_DISPLAY_IFC_CTRL1, 0x0001), /* 16 bit RGB */
    REG_SET(ILI9328_FMARK_POS, 0x0000),
    REG_SET(ILI9328_RGB_DISPLAY_IFC_CTRL2, 0x0000), /* RGB interface polarity */

    /* Power On Sequence */
    REG_SET(ILI9328_POWER_CTRL1, 0x0000),
    REG_SET(ILI9328_POWER_CTRL2, 0x0007),
    REG_SET(ILI9328_POWER_CTRL3, 0x0000),
    REG_SET(ILI9328_POWER_CTRL4, 0x0000),

    DELAY_MS(200),

    REG_SET(ILI9328_POWER_CTRL1, 0x1690),
    REG_SET(ILI9328_POWER_CTRL2, 0x0227),

    DELAY_MS(50),

    REG_SET(ILI9328_POWER_CTRL3, 0x001a),

    DELAY_MS(50),

    REG_SET(ILI9328_POWER_CTRL4, 0x1800),
    REG_SET(ILI9328_POWER_CTRL7, 0x002a),
    REG_SET(ILI9328_FRAME_RATE_AND_COLOR_CTRL, 0x0d), /* 93 frames */

    DELAY_MS(50),

    /* Gamma Curve */
    REG_SET(ILI9328_GAMMA_CTRL1, 0x0007),
    REG_SET(ILI9328_GAMMA_CTRL2, 0x0605),
    REG_SET(ILI9328_GAMMA_CTRL3, 0x0106),
    REG_SET(ILI9328_GAMMA_CTRL4, 0x0206),
    REG_SET(ILI9328_GAMMA_CTRL5, 0x0808),
    REG_SET(ILI9328_GAMMA_CTRL6, 0x0007),
    REG_SET(ILI9328_GAMMA_CTRL7, 0x0201),
    REG_SET(ILI9328_GAMMA_CTRL8, 0x0007),
    REG_SET(ILI9328_GAMMA_CTRL9, 0x0602),
    REG_SET(ILI9328_GAMMA_CTRL10, 0x0808),

    DELAY_MS(50),

    /* GRAM Area */
    REG_SET(ILI9328_HSTART, 0x0000),
    REG_SET(ILI9328_HEND, 239),
    REG_SET(ILI9328_VSTART, 0x0000),
    REG_SET(ILI9328_VEND, 319),

    /* Gate Scan Control*/
    REG_SET(ILI9328_GATE_SCAN_CTRL1, 0xa700),
    REG_SET(ILI9328_GATE_SCAN_CTRL2, 0x0001),
    REG_SET(ILI9328_GATE_SCAN_CTRL3, 0x0000),

    /* Partial Display Control - not used initially */
    REG_SET(ILI9328_PARTIAL_IMAGE1_DISPLAY_POS, 0x0000),
    REG_SET(ILI9328_PARTIAL_IMAGE1_RAM_START, 0x0000),
    REG_SET(ILI9328_PARTIAL_IMAGE1_RAM_END, 0x0000),
    REG_SET(ILI9328_PARTIAL_IMAGE2_DISPLAY_POS, 0x0000),
    REG_SET(ILI9328_PARTIAL_IMAGE2_RAM_START, 0x0000),
    REG_SET(ILI9328_PARTIAL_IMAGE2_RAM_END, 0x0000),

    /* Panel Control */
    REG_SET(ILI9328_PANEL_IFC_CTRL1, 0x0010),
    REG_SET(ILI9328_PANEL_IFC_CTRL2, 0x0000),
    REG_SET(ILI9328_PANEL_IFC_CTRL4, 0x0110),
    REG_SET(ILI9328_PANEL_IFC_CTRL5, 0x0000),

    /* display On */
    REG_SET(ILI9328_DISPLAY_CTRL1, 0x0133),

    /* Set Cursor to Origin */
    REG_SET(ILI9328_HADDR_SET, 0x0000),
    REG_SET(ILI9328_VADDR_SET, 0x0000),

    DELAY_MS(20),

    SEQUENCE_END
};
